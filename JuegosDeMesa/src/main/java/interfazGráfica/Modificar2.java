//            }
        id = Integer.parseInt(jLabel22.getText());
        nombreFinal = jTextField1Nombre.getText();
        disenador = jTextField1Disenador.getText();

        tematica = Temática.getText();
        dueno = jTextField1Dueno.getText();
        expansionDe = jComboBox1listadojuegos.getSelectedItem().toString();
        descripcion = jTextArea1.getText();

        if (nombreFinal.isEmpty() || disenador.isEmpty() || tematica.isEmpty() || dueno.isEmpty() || expansionDe.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se puede dejar campos en blanco", "Datos vacíos", JOptionPane.WARNING_MESSAGE);

        } else {
            boolean ok = true;
            Calendar cal = Calendar.getInstance();
            try {
                do {
                    año = Integer.parseInt(jTextField1Año.getText());
                    numJugMax = Integer.parseInt(jTextField1numJugMax.getText());
                    numJugMin = Integer.parseInt(jTextField1JugMin.getText());
                    duracion = Integer.parseInt(jTextField1Dura.getText());
                    dificultad = Integer.parseInt(jTextField1Difi.getText());
                    estrategia = Integer.parseInt(jTextField2estra.getText());
                    suerte = Integer.parseInt(jTextField1Suert.getText());
                    interaccion = Integer.parseInt(jTextField1Inter.getText());
                    try {
                        if (año > cal.get(Calendar.YEAR) + 1 || año < 1980 || numJugMax < numJugMin) {
                            JOptionPane.showMessageDialog(this, "La año debe estar entre 1980 ", "Error ", JOptionPane.WARNING_MESSAGE);
                            return;
                        } else {
                            ok = false;
                        }

                        if (duracion < 5 || duracion > 250) {
                            JOptionPane.showMessageDialog(this, "El numero debe ser entre 5 y 250 ", "Error ", JOptionPane.WARNING_MESSAGE);
                            return;
                        } else {
                            ok = false;
                        }
                        if (dificultad > 5 || dificultad < 0) {
                            JOptionPane.showMessageDialog(this, "El numero debe ser entre 0 y 5 ", "Error ", JOptionPane.WARNING_MESSAGE);
                            return;
                        } else {
                            ok = false;
                        }
                        if (estrategia > 5 || estrategia < 0) {
                            JOptionPane.showMessageDialog(this, "El numero debe ser entre 0 y 5 ", "Error ", JOptionPane.WARNING_MESSAGE);
                            return;
                        } else {
                            ok = false;
                        }
                        if (suerte > 5 || suerte < 0) {
                            JOptionPane.showMessageDialog(this, "El numero debe ser entre 0 y 5 ", "Error ", JOptionPane.WARNING_MESSAGE);
                            return;
                        } else {
                            ok = false;
                        }
                        if (interaccion > 5 || interaccion < 0) {
                            JOptionPane.showMessageDialog(this, "El numero debe ser entre 0 y 5 ", "Error ", JOptionPane.WARNING_MESSAGE);
                            return;
                        } else {
                            ok = false;
                        }
                        
                        if(jComboBox2esExpansion.getSelectedItem().toString() == "si"){
                            expansion = true;
                        }else{
                            expansion = false;
                        }

                        Juego a = new Juego(id);

                        a.setNombre(nombreFinal);
                        a.setDisenador(disenador);
                        a.setAño(año);
                        a.setDueno(dueno);
                        a.setDuracion(duracion);
                        a.setNumJugMax(numJugMax);
                        a.setNumJugMin(numJugMin);
                        a.setDificultad(dificultad);
                        a.setEstrategia(estrategia);
                        a.setSuerte(suerte);
                        a.setInteraccion(interaccion);
                        a.setExpansion(expansion);
                        a.setExpansionDe(expansionDe);
                        a.setDescripcion(descripcion);
                        a.setTematica(tematica);

                        try {

                            int juegoActualizado = daoEst.actualizarTodo(a);
                            if (juegoActualizado == 1) {
                                JOptionPane.showMessageDialog(this, "Juego actualizado", "Juego actualizado", JOptionPane.INFORMATION_MESSAGE);
                                System.out.println("El juego " + a.getNombre() + " ha sido actualizado.");
                            } else {
                                System.out.println("El juego " + a.getNombre() + " no ha sido actualizado.");
                            }
                            dispose();
                        } catch (Exception e) {
                            System.out.println("ERROR!!!  El juego " + a.getNombre() + " no ha sido actualizado.");
                        }

                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(this, "Los campos numericos deben de ser un número", "Error de tipo de dato.", JOptionPane.WARNING_MESSAGE);
                        return;     //Voy al final
                    }

                } while (ok == true);

            } catch (Exception e) {

            }
        }

