    private void actualizarComboBox() {

        if (userInput.length() == 0) {
            jComboBox1Juegosencontrados.removeAllItems();
            jComboBox2esExpansion.setEnabled(false);
            return;
        }
        try {
//            juegos = daoEst.buscarporNombreConLetras(userInput.toString() + "%");
            juegos = daoEst.buscarporNombreConLetras(userInput.toString());
            

        } catch (ExcepcionMia ex) {
            Logger.getLogger(Modificar2.class.getName()).log(Level.SEVERE, null, ex);
        }

        jComboBox1Juegosencontrados.removeAllItems();
        if (!juegos.isEmpty()) {
            for (Juego j : juegos) {
                jComboBox1Juegosencontrados.addItem(j.getNombre());
                jComboBox2esExpansion.setEnabled(true);
                jComboBox1Juegosencontrados.showPopup();
            }
        } else {
            jComboBox1Juegosencontrados.addItem("---");
        }

        System.out.println("Texto actual: " + userInput.toString());
    }

