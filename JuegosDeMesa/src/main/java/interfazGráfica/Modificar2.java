    // Método para actualizar los TextFields basados en el juego seleccionado
    private void actualizarTextFields() {
        if (jComboBox1Juegosencontrados.getSelectedItem() != null) {
            juegoSelecionado = jComboBox1Juegosencontrados.getSelectedItem().toString();
            // Buscar el juego seleccionado en la lista de juegos
            for (Juego juego : juegos) {
                if (juego.getNombre().equals(juegoSelecionado)) {
                    j = juego;
                    break;
                }
            }
            if (j != null) {
                jLabel22.setText(String.valueOf(j.getId()));
                jTextField1Nombre.setText(j.getNombre());
                jTextField1Disenador.setText(j.getDisenador());
                jTextField1Año.setText(String.valueOf(j.getAño()));
                Temática.setText(j.getTematica());
                jTextField1numJugMax.setText(String.valueOf(j.getNumJugMax()));
                jTextField1JugMin.setText(String.valueOf(j.getNumJugMin()));
                jTextField1Dura.setText(String.valueOf(j.getDuracion()));    //Por si da problemas el casateo
                jTextField1Difi.setText(String.valueOf(j.getDificultad()));
                jTextField2estra.setText(String.valueOf(j.getEstrategia()));
                jTextField1Suert.setText(String.valueOf(j.getSuerte()));
                jTextField1Inter.setText(String.valueOf(j.getInteraccion()));
                jTextArea1.setText(j.getDescripcion());
                jTextField1Dueno.setText(j.getDueño());

                if (j.isExpansion() == true) {
                    jComboBox2esExpansion.setSelectedItem("si");
                    jComboBox1listadojuegos.setEnabled(true);
                    jComboBox1listadojuegos.setSelectedItem(j.getExpansionDe());
                } else {
                    jComboBox2esExpansion.setSelectedItem("no");
                    jComboBox1listadojuegos.setEnabled(false);
                }

            }
        }else{
            jTextField1Nombre.setText("");
             jTextField1numJugMax.setText("");
                jTextField1JugMin.setText("");
                jTextField1Dura.setText("");
                jTextField1Difi.setText("");
                jTextField2estra.setText("");
                jTextField1Suert.setText("");
                jTextField1Inter.setText("");
                jTextArea1.setText("");
                jLabel18.setText("");
                jTextField1Dueno.setText("");
                jComboBox2esExpansion.setEnabled(false);
                jComboBox1listadojuegos.setEnabled(false);
                
                jLabel22.setText("");
                jTextField1Disenador.setText("");
                jTextField1Año.setText("");
                Temática.setText("");
                
        }
    }

